    "        \"\"\"Run the complete RAG pipeline and return a validated answer.",
    "",                
    "        The method retrieves relevant documents, generates a candidate answer,",
    "        and applies a reliability gate based on retrieval alignment and model confidence.",
    "",                
    "        Args:",
    "            question: The user's question.",
    "            n_results: Number of documents to retrieve from ChromaDB.",
