    "",                
    "        Args:",
    "            query: Question text used for semantic retrieval.",
    "            n_results: Number of top documents to return.",
    "",                
    "        Returns:",
    "            Dictionary containing retrieved documents, metadatas, and distance scores.",
    "        \"\"\"",
    "        results = self.collection.query(",
    "            query_texts=[query],",
    "            n_results=n_results,",
    "            include=[\"documents\", \"metadatas\", \"distances\"]",
    "        )",
    "        return results",
    "",
    "    def generate_answer(self, question: str, context: str) -> Dict:",
    "        \"\"\"Generate an extractive answer from retrieved context text.",
    "",                
    "        The model returns logits for start and end token positions. The selected",
    "        answer span is decoded from the token indices and converted into a",
    "        normalized confidence score.",
    "",                
    "        Args:",
    "            question: The original user question.",
    "            context: Consolidated text retrieved from the knowledge base.",
    "",                
    "        Returns:",
    "            Dictionary with the extracted answer text and model confidence score.",
    "        \"\"\"",
    "        extraction = self.qa_model(question=question, context=context)",
